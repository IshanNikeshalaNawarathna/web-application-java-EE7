package model;

import java.io.Serializable;

/**
 *
 * @author Ishan
 */
public class User implements Serializable{
    
    private String mobile;
    private String name;

    public User() {
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    
}
